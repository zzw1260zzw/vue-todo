<template>
  <div class="todo-item" :class="{'is-complete':todo.completed}">
    <p class>
      <input type="checkbox" @change="markComplete">
      {{todo.title}}
      <button class="del" @click="$emit('deleteItem',todo.id)">x</button>
      </p>
  </div>
</template>

<script>
export default {
  name:"Todo",
  props:["todo"],
  methods:{
    markComplete(){
      this.todo.completed=!this.todo.completed;
    }
  }
};
</script>

<style scoped>
  .todo-item{
    background: #4f4f4f;
    padding:10px;
    border-bottom: 1px #ccc dotted;
  }
  
  .is-complete{
    text-decoration:line-through;
  }

  .del{
    background: #ff0000;
    color: #fff;
    border:none;
    padding:5px 9px;
    border-radius: 50%;
    cursor: pointer;
    float: right;
  }

</style>